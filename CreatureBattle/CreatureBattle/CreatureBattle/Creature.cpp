//******************************************************************************
//******************************************************************************
//                          Take Attack Damage
//Function- Execution Begins Here
//Inputs
//      
//Outputs
//      
////////////////////////////////////////////////////////////////////////////////
void Creature::tkAdmg(int A,int B) {
//Take Attack Damage
	int tDmg = A - (cArmor*.5f - B);
	cHP -= tDmg;
}
//******************************************************************************
//******************************************************************************
//                          Take Magic Damage
//Function- Execution Begins Here
//Inputs
//      
//Outputs
//      
////////////////////////////////////////////////////////////////////////////////
void Creature::tkMdmg(int A, int B) {
	//Take Attack Damage
	int tDmg = A - (cResist*.5f - B);
	cHP -= tDmg;
}