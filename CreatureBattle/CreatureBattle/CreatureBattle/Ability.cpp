ShockNova::ShockNova() {
//Intialize the Skill Stats
	eCst = 100;
	speed = 50;
	aDmg = 0;
	mDmg = 100;
}
void ShockNova::stEffct(Creature* A,Creature* B) {
//Ability Effect
	//Deal Magic Damage
	int Dmg = mDmg + (A->cMagic*1.0f);
	B->tkMdmg(Dmg, A->mPen);
}


